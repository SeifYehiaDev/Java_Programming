package Chapter_04_Examples;

public class Example_04_04 {
    public static void main(String[] args) {
        System.out.println("Math.max(2, 3) = " + Math.max(2, 3)); // return 3 ====> integer
        System.out.println("Math.max(2.5, 3) = " + Math.max(2.5, 3)); // return 3.0 ====> double
        System.out.println("Math.min(2.5, 4.6) = " + Math.min(2.5, 4.6)); // return 2.5 ====> double
        System.out.println("Math.abs(-2) = " + Math.abs(-2)); // return 2 ====> integer
        System.out.println("Math.abs(-2.1) = " + Math.abs(-2.1)); // return 2.1 ====> integer
    }
}

//                                                 _Output_
/*
Math.max(2, 3) = 3
Math.max(2.5, 3) = 3.0
Math.min(2.5, 4.6) = 2.5
Math.abs(-2) = 2
Math.abs(-2.1) = 2.1
*/