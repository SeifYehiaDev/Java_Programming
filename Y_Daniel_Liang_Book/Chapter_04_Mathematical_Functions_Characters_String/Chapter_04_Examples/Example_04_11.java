package Chapter_04_Examples;

public class Example_04_11 {
    public static void main(String[] args) {
        String message = "Welcome to Java";
        System.out.println(message);
        /*
         * String: String is a predefined class in the Java library, just like the classes System and Scanner.
         * The String type is not a primitive type. It is known as a reference type.
         *
         * The variable declared by a reference type is known as a reference variable that references an object.
         * Here, message is a reference variable that references a string object with contents Welcome to Java.
         * */
    }
}

//                                                 _Output_
/*
Welcome to Java
 */