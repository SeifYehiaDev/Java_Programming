package Chapter_02_Examples;

public class Example_02_06 {
    public static void main(String[] args) {
        System.out.println("________________ x * 10 ^ positive number ________________");
        System.out.println(1.23456E2);
        System.out.println(1.23456E+2);
        System.out.println(1.23456e2);
        System.out.println(1.23456e+2);
        System.out.println("________________ x * 10 ^ negative number ________________");
        System.out.println(1.23456E-2);
        System.out.println(1.23456e-2);
    }
}

/*                                                 _Output_
 * ________________ x * 10 ^ positive number ________________
 * 123.456
 * 123.456
 * 123.456
 * 123.456
 * ________________ x * 10 ^ negative number ________________
 * 0.0123456
 * 0.0123456
 * */