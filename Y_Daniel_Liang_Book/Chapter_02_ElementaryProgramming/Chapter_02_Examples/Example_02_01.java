package Chapter_02_Examples;

public class Example_02_01 {
    public static void main(String[] args) {
        double i = 50.0;
        double k = i + 50.0; // k = 100.0
        double j = k + 1;    // j = 101.0
        System.out.println("j is " + j + " and k is " + k);
    }
}

/*                                                 _Output_
 * j is 101.0 and k is 100.0
 * */