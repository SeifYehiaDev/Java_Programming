package Chapter_02_Examples;

public class Example_02_04 {
    public static void main(String[] args) {
        System.out.println("1 % 5 = " + 1 % 5);
        System.out.println("2 % 5 = " + 2 % 5);
        System.out.println("3 % 5 = " + 3 % 5);
        System.out.println("4 % 5 = " + 4 % 5);
        System.out.println("5 % 5 = " + 5 % 5);
        System.out.println("_____________________________________");
        System.out.println("-7 % 3 = " + -7 % 3);
        System.out.println("-12 % 4 = " + -12 % 4);
        System.out.println("-26 % -8 = " + -26 % -8);
        System.out.println("20 % -13 = " + 20 % -13);
        System.out.println("______________ odd number % 2 is always 1 ______________");
        System.out.println("5 % 2 = " + 5 % 2);
        System.out.println("9 % 2 = " + 9 % 2);
        System.out.println("231 % 2 = " + 231 % 2);
        System.out.println("______________ even number % 2 is always 0 ______________");
        System.out.println("4 % 2 = " + 4 % 2);
        System.out.println("6 % 2 = " + 6 % 2);
        System.out.println("8 % 2 = " + 8 % 2);
    }
}

