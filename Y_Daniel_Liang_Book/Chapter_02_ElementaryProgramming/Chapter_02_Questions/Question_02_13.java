package Chapter_02_Questions;

/*                                                 _Question_
 * What is the result of 25 / 4?
 * How would you rewrite the expression if you wished the result to be a floating-point number?
 * */
//                                                 _Solution_
public class Question_02_13 {
    public static void main(String[] args) {
        System.out.println("25 / 4 = " + 25 / 4);
        /*
        * integer / integer = integer
        * 25 / 4 = 6
        * */
        System.out.println("25.0 / 4 = " + 25.0 / 4);
        /*
         * double / integer = double
         * 25.0 / 4 = 6.25
         * */
    }
}

/*                                                 _Output_
 * 25 / 4 = 6
 * 25.0 / 4 = 6.25
 * */