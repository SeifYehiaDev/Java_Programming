package Chapter_03_Examples;

public class Example_03_05 {
    public static void main(String[] args) {
        int x = 5;
        boolean isOdd = x % 2 == 1;
        System.out.println("x = " + x);
        if (isOdd = false)
            System.out.println("x is odd number");
        else
            System.out.println("x is even number");
    }
}

/*                                                 _Output_
 * x = 5
 * x is even number
 * */